//Logical Operators
/*

    AND OPERATOR : &&
    OR OPERATOR  : ||
    NOT OPERATOR : !
 */

//AND OPERATOR

console.log(
      (true) && (false) 

);  
//prints false as both the conditions are'nt true

console.log(
     
   ( true ) && ( true )
)
//prints true as both the conditions are true 


//OR OPERATOR

console.log(
    (true)  ||  (false)
);

//prints true as one of the condition is true

//NOT OPERATOR

console.log(!(true));


